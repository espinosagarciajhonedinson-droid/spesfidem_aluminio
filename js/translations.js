// ===== FUNCIONES DE TRADUCCIÓN =====

function getCurrentLanguage() {
    return localStorage.getItem('siteLanguage') || 'es';
}

function setLanguage(lang) {
    localStorage.setItem('siteLanguage', lang);
    applyTranslations(lang);
    document.getElementById('langMenu')?.classList.remove('active');
}

function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.innerHTML.includes('<br>')) {
                const parts = translations[lang][key].split(' ');
                if (parts.length > 1) {
                    element.innerHTML = parts[0] + ' <br>' + parts.slice(1).join(' ');
                } else {
                    element.textContent = translations[lang][key];
                }
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    updateLanguageSelector(lang);
}

function updateLanguageSelector(lang) {
    const selector = document.getElementById('currentLang');
    if (selector) {
        const flags = {
            'es': '🇪🇸', 'en': '🇺🇸', 'pt': '🇧🇷', 'fr': '🇫🇷', 'zh': '🇨🇳',
            'de': '🇩🇪', 'it': '🇮🇹', 'ja': '🇯🇵', 'ru': '🇷🇺', 'ar': '🇸🇦'
        };
        const names = {
            'es': 'Español', 'en': 'English', 'pt': 'Português', 'fr': 'Français', 'zh': '中文',
            'de': 'Deutsch', 'it': 'Italiano', 'ja': '日本語', 'ru': 'Русский', 'ar': 'العربية'
        };
        selector.innerHTML = `
            <span class="lang-globe">🌐</span>
            <span class="lang-flag-current">${flags[lang]}</span>
            <span class="lang-name-current">${names[lang]}</span>
            <i class="fas fa-chevron-down lang-arrow"></i>
        `;
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(getCurrentLanguage());
});
